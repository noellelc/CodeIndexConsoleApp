namespace TesterConsoleApp
{
    public class IndexFormatRange
    {
        /// <summary>
        /// Gets or sets the value for the starting line
        /// </summary>
        public int sl { get; set; }

        /// <summary>
        /// Gets or sets the value for the starting character
        /// </summary>
        public int sc { get; set; }

        /// <summary>
        /// Gets or sets the value for the ending line
        /// </summary>
        public int el { get; set; }

        /// <summary>
        /// Gets or sets the value for the ending character
        /// </summary>
        public int ec { get; set; }

        /// <summary>
        /// Gets or sets the value for the text
        /// </summary>
        public string t { get; set; }
    }
}
